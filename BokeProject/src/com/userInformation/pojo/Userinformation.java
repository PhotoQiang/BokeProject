package com.userInformation.pojo;

public class Userinformation {
	private  int InformationId;
	private  int UserId;
	private  String TickName;
	private  String Telphone;
	private  String Hobby;
	

	public Userinformation(int userId, String tickName, String telphone,
			String hobby) {
		super();
		UserId = userId;
		TickName = tickName;
		Telphone = telphone;
		Hobby = hobby;
	}
	public Userinformation( String tickName, String telphone,
			String hobby,int userId) {
		super();
		UserId = userId;
		TickName = tickName;
		Telphone = telphone;
		Hobby = hobby;
	}
	public Userinformation(int informationId, int userId, String tickName,
			String telphone, String hobby) {
		super();
		InformationId = informationId;
		UserId = userId;
		TickName = tickName;
		Telphone = telphone;
		Hobby = hobby;
	}
	public int getInformationId() {
		return InformationId;
	}
	public void setInformationId(int informationId) {
		InformationId = informationId;
	}
	public int getUserId() {
		return UserId;
	}
	public void setUserId(int userId) {
		UserId = userId;
	}
	public String getTickName() {
		return TickName;
	}
	public void setTickName(String tickName) {
		TickName = tickName;
	}
	public String getTelphone() {
		return Telphone;
	}
	public void setTelphone(String telphone) {
		Telphone = telphone;
	}
	public String getHobby() {
		return Hobby;
	}
	public void setHobby(String hobby) {
		Hobby = hobby;
	}

	

}
