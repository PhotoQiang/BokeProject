package com.message.pojo;

public class Reply {

	private int replyId;
	private String replyContent;
	private int rmessageId;
	private int userId;
	private int friendId;
	
	public Reply(){
		
	}
	
	
	public Reply(int rmessageId, int userId, int friendId) {
		super();
		this.rmessageId = rmessageId;
		this.userId = userId;
		this.friendId = friendId;
	}




	public Reply(String replyContent, int rmessageId, int userId, int friendId) {
		super();
		this.replyContent = replyContent;
		this.rmessageId = rmessageId;
		this.userId = userId;
		this.friendId = friendId;
	}



	public Reply(int replyId, String replyContent, int rmessageId, int userId,
			int friendId) {
		super();
		this.replyId = replyId;
		this.replyContent = replyContent;
		this.rmessageId = rmessageId;
		this.userId = userId;
		this.friendId = friendId;
	}



	public int getReplyId() {
		return replyId;
	}
	public void setReplyId(int replyId) {
		this.replyId = replyId;
	}
	public String getReplyContent() {
		return replyContent;
	}
	public void setReplyContent(String replyContent) {
		this.replyContent = replyContent;
	}
	public int getRmessageId() {
		return rmessageId;
	}
	public void setRmessageId(int rmessageId) {
		this.rmessageId = rmessageId;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getFriendId() {
		return friendId;
	}
	public void setFriendId(int friendId) {
		this.friendId = friendId;
	}
	
	
}
