package com.message.pojo;

import java.io.Serializable;

public class Message implements Serializable{
    private int messageId;
    private int useId;
    private String messageContent;
    private int friendId;
    private String friendName;
    private int toUserId;

	public Message() {
		super();
	}
	
	
	public Message(int messageId) {
		super();
		this.messageId = messageId;
	}
	
	public Message(int userId, String messageContent,
			int friendId) {
		super();
		this.useId = userId;
		this.messageContent = messageContent;
		this.friendId = friendId;
	}
	
	
	public Message( int useId, String messageContent,
			int friendId, String friendName, int toUserId) {
		super();
		this.useId = useId;
		this.messageContent = messageContent;
		this.friendId = friendId;
		this.friendName = friendName;
		this.toUserId = toUserId;
	}
	
	public Message(int messageId, int useId, String messageContent,
			int friendId, String friendName, int toUserId) {
		super();
		this.messageId = messageId;
		this.useId = useId;
		this.messageContent = messageContent;
		this.friendId = friendId;
		this.friendName = friendName;
		this.toUserId = toUserId;
	}


	public Message(int messageId, int userId, String messageContent,
			int friendId) {
		super();
		this.messageId = messageId;
		this.useId = userId;
		this.messageContent = messageContent;
		this.friendId = friendId;
	}

	public Message( int userId, String MessageContent,
			int friendId, String friendName) {
		super();
		this.useId = userId;
		this.messageContent = MessageContent;
		this.friendId = friendId;
		this.friendName = friendName;
	}


	public Message(int messageId, int userId, String messageContent,
			int friendId, String friendName) {
		super();
		this.messageId = messageId;
		this.useId = userId;
		this.messageContent = messageContent;
		this.friendId = friendId;
		this.friendName = friendName;
	}

	public int getFriendId() {
		return friendId;
	}
	public void setFriendId(int friendId) {
		this.friendId = friendId;
	}
	
	public String getFriendName() {
		return friendName;
	}


	public void setFriendName(String friendName) {
		this.friendName = friendName;
	}


	public String getMessageContent() {
		return messageContent;
	}public void setMessageContent(String messageContent) {
		this.messageContent = messageContent;
	}public int getMessageId() {
		return messageId;
	}
	public void setMessageId(int messageId) {
		this.messageId = messageId;
	}
	public int getUseId() {
		return useId;
	}
	public void setUseId(int useId) {
		this.useId = useId;
	}
	


	public int getToUserId() {
		return toUserId;
	}


	public void setToUserId(int toUserId) {
		this.toUserId = toUserId;
	}


	@Override
	public String toString() {
		return "Message [messageId=" + messageId + ", useId=" + useId
				+ ", messageContent=" + messageContent + ", friendId="
				+ friendId + ", friendName=" + friendName + ", toUserId="
				+ toUserId + "]";
	}


	
    
}
